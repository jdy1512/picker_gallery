package com.allchange.home.result;

public class RoomResult {

	private String result;
	private String result_msg;
	private String male_no;
	private String female_no;
	private String user_check_indate;
	private String user_check_outdate;

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getResult_msg() {
		return result_msg;
	}

	public void setResult_msg(String result_msg) {
		this.result_msg = result_msg;
	}

	public String getMale_no() {
		return male_no;
	}

	public void setMale_no(String male_no) {
		this.male_no = male_no;
	}

	public String getFemale_no() {
		return female_no;
	}

	public void setFemale_no(String female_no) {
		this.female_no = female_no;
	}

	public String getUser_check_indate() {
		return user_check_indate;
	}

	public void setUser_check_indate(String user_check_indate) {
		this.user_check_indate = user_check_indate;
	}

	public String getUser_check_outdate() {
		return user_check_outdate;
	}

	public void setUser_check_outdate(String user_check_outdate) {
		this.user_check_outdate = user_check_outdate;
	}

}
